    private void setDatos() {
        float total = 0, abono = 0;
        final DecimalFormat formato = new DecimalFormat("#,###.00");
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        for (VariedadesVentas ventas : obtenerVenta()) {
            model.addRow(dato);
            model.setValueAt(ventas.getCodigoventa(), model.getRowCount() - 1, 0);
            model.setValueAt(Funciones.ddMMyyyy.format(ventas.getFechaventa()), model.getRowCount() - 1, 1);
            model.setValueAt(formato.format(ventas.getValortotal()), model.getRowCount() - 1, 2);
            total = total + ventas.getValortotal();
            for (VariedadesAbono va : obtenerAbono(ventas)) {
                abono = abono + va.getAbono();
            }
        }
        jLabel2.setText("El cliente adeuda un total de: "+formato.format(total-abono));
    }

    private List<VariedadesVentas> obtenerVenta() {
        EntityManager em = vvjc.getEntityManager();
        try {
            return em.createQuery("SELECT f FROM VariedadesVentas f WHERE f.codcliente=:idusua AND f.estado='3'")
                    .setHint("javax.persistence.cache.storeMode", "REFRESH")
                    .setParameter("idusua", documento)
                    .getResultList();
        } finally {
            em.close();
        }
    }

    private List<VariedadesAbono> obtenerAbono(VariedadesVentas venta) {
        EntityManager em = vvjc.getEntityManager();
        try {
            return em.createQuery("SELECT f FROM VariedadesAbono f WHERE f.idventa=:abono")
                    .setHint("javax.persistence.cache.storeMode", "REFRESH")
                    .setParameter("abono", venta)
                    .getResultList();
        } finally {
            em.close();
        }
    }

